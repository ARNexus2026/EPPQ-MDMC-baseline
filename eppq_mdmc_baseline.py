import numpy as np
import networkx as nx
import matplotlib.pyplot as plt
from tqdm import tqdm

# -----------------------------------------------------------------------------
# 1. Simulação da Dinâmica PMAH (Metropolis-Hastings com Temperatura)
# -----------------------------------------------------------------------------

def pmah_vacuum_generator(N, m, lambda_val, steps=8000, T=2.0, seed=42):
    """
    T: Temperatura termodinâmica do banho. Controla o quão fácil o grafo
       pode aceitar trocas que pioram momentaneamente a energia para evitar mínimos locais.
    """
    np.random.seed(seed)

    # Inicializa BA
    G = nx.barabasi_albert_graph(N, m, seed=seed)
    target_degrees = {node: deg for node, deg in G.degree()}

    for (i, j) in G.edges():
        G[i][j]['c'] = np.random.uniform(0, 1)
        G[j][i]['c'] = G[i][j]['c']

    root = 0
    try:
        historical_depth = nx.shortest_path_length(G, source=root)
        h = np.array([historical_depth.get(i, np.inf) for i in range(N)])
    except:
        h = np.full(N, np.inf)
        h[root] = 0

    for step in tqdm(range(steps), desc=f"PMAH λ={lambda_val:.2f}"):
        i = np.random.randint(0, N)

        if G.degree(i) > 0:
            j = np.random.choice(list(G.neighbors(i)))
            neighbors_of_j = list(G.neighbors(j))
            if len(neighbors_of_j) > 1:
                k = np.random.choice([x for x in neighbors_of_j if x != i])

                # Custo Histórico
                current_h_i = h[i]
                new_h_i_est = min(current_h_i, h[k] + 1, h[j] + 1)
                delta_C = max(0, new_h_i_est - current_h_i)

                # Custo de Interface
                deg_j = G.degree(j)
                deg_k = G.degree(k)
                target_j = target_degrees[j]
                target_k = target_degrees[k]

                cost_j_old = (deg_j - target_j)**2
                cost_j_new = (deg_j - 1 - target_j)**2
                cost_k_old = (deg_k - target_k)**2
                cost_k_new = (deg_k + 1 - target_k)**2
                delta_I = (cost_j_new - cost_j_old) + (cost_k_new - cost_k_old)

                # Variação total da Ação
                delta_A = delta_I + lambda_val * delta_C

                # Aceitação de Boltzmann rigorosa
                if delta_A < 0 or np.random.rand() < np.exp(-delta_A / T):
                    G.remove_edge(i, j)
                    G.add_edge(i, k)
                    G[i][k]['c'] = np.random.uniform(0, 1)
                    G[k][i]['c'] = G[i][k]['c']

                    try:
                        new_h = nx.shortest_path_length(G, source=root)
                        h = np.array([new_h.get(v, np.inf) for v in range(N)])
                    except:
                        pass

    degrees = np.array([deg for _, deg in G.degree()])
    mean_deg = np.mean(degrees)

    k_min = np.percentile(degrees, 75)
    tail_degrees = degrees[degrees >= k_min]

    if len(tail_degrees) > 3 and k_min > 0:
        gamma_est = 1.0 + len(tail_degrees) / np.sum(np.log(tail_degrees / k_min))
        beta_est = gamma_est - 1.0
    else:
        beta_est = 1.6

    return G, beta_est, mean_deg

