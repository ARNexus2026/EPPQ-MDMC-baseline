def run_lambda_validation():
    N = 500
    m = 2
    # Esticamos o range de lambda para forçar o grafo a reagir
    lambda_values = np.linspace(0.5, 6.0, 8)
    trials_per_lambda = 3

    results = {"lambda": [], "beta": [], "mean_deg": []}

    print("=== EPPQ λ-VALIDATION PIPELINE (BOLTZMANN) ===")
    print(f"N = {N}, m = {m}\n")

    for lam in lambda_values:
        beta_list = []
        deg_list = []
        for trial in range(trials_per_lambda):
            seed = 42 + int(lam * 10) + trial
            G, beta_est, mean_deg = pmah_vacuum_generator(N, m, lam, steps=8000, T=3.0, seed=seed)
            if G is not None:
                beta_list.append(beta_est)
                deg_list.append(mean_deg)

        if beta_list:
            avg_beta = np.mean(beta_list)
            avg_deg = np.mean(deg_list)
            results["lambda"].append(lam)
            results["beta"].append(avg_beta)
            results["mean_deg"].append(avg_deg)
            print(f"λ = {lam:.2f} -> β = {avg_beta:.3f}, ⟨d⟩ = {avg_deg:.2f}, β⟨d⟩ = {avg_beta * avg_deg:.3f}")

    return results

def analyze_and_plot(results):
    lambdas = np.array(results["lambda"])
    betas = np.array(results["beta"])
    mean_degs = np.array(results["mean_deg"])
    product = betas * mean_degs

    coeffs = np.polyfit(product, lambdas, 1)
    K_fit = coeffs[0]
    intercept = coeffs[1]

    product_fit = np.linspace(min(product), max(product), 100)
    lambda_fit = K_fit * product_fit + intercept

    plt.figure(figsize=(8, 6))
    plt.scatter(product, lambdas, color='blue', s=80, label='Simulações PMAH (Boltzmann)')
    plt.plot(product_fit, lambda_fit, 'r--', label=rf'Ajuste: $\lambda = {K_fit:.3f} \cdot \beta\langle d \rangle + {intercept:.3f}$')

    # Correção dos Raw Strings (r'') para evitar o SyntaxWarning
    plt.xlabel(r'$\beta \cdot \langle d \rangle$', fontsize=14)
    plt.ylabel(r'$\lambda$ (Inércia Histórica)', fontsize=14)
    plt.title(r'Validação Dinâmica da Constante Fundamental de Inércia', fontsize=16)

    plt.legend()
    plt.grid(alpha=0.3)
    plt.tight_layout()
    plt.savefig('lambda_validation_final.png', dpi=150)
    plt.show()

    print("\n=== RESULTADOS DA VALIDAÇÃO ===")
    print(f"Constante K extraída: {K_fit:.4f}")
    print(f"Intercepto: {intercept:.4f}")
    print(f"Coeficiente de correlação R²: {np.corrcoef(product, lambdas)[0,1]**2:.4f}")

    return K_fit

if __name__ == "__main__":
    results = run_lambda_validation()
    K = analyze_and_plot(results)